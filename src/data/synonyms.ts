type Entry = readonly [string, string[], string];

function makeEntries(languageId: string, prefix: string, entries: Entry[]): Synonym[] {
  return entries.map(([word, synonyms, translation], i) => ({
    id: `syn-${prefix}-${i + 1}`,
    languageId,
    word,
    synonyms,
    translation,
  }));
}

